RomDevice::RomDevice(MacSystem* system) : system_(system) {}

RomDevice::~RomDevice() {}

void RomDevice::InstallSonyPatch() {
  unsigned char* rom = system_->get_rom();
  unsigned char* ram = system_->get_ram();

  DLOG( "RomDevice::InstallSonyPatch patching ROM at %p\n", rom);
  if (!rom) {
    DLOG( "RomDevice: ROM is NULL!\n");
    return;
  }

  // Bypass RTC loop at 0x4004f0
  put_mem_word(rom + 0x04f0, 0x4e75);

  // Bypass RAM test/clear at 0x401d3e
  put_mem_word(rom + 0x1d3e, 0x4e75);

  // Bypass 7-minute hardware power-on delay at 0x1e34
  put_mem_word(rom + 0x1e34, 0x4e71);
  put_mem_word(rom + 0x1e36, 0x4e71);

  // Bypass Delay Trap and power-on diagnostic loop at 0x4029ea by making it
  // return immediately
  put_mem_word(rom + 0x29ea, 0x4e75);

  // Bypass 0x24fe delay subroutine completely by jumping to A6 (return address)
  put_mem_word(rom + 0x24fe, 0x4ed6);

  // Bypass secondary 2.1 second delay loop caller at 0x2278
  put_mem_word(rom + 0x2278, 0x4e71);
  put_mem_word(rom + 0x227a, 0x4e71);

  // Patch ROM at 0x401be6 to initialize A6, clear Overlay, and jump to 0x401d04
  put_mem_word(rom + 0x1be6, 0x2c7c);
  put_mem_word(rom + 0x1be8, 0x0040);
  put_mem_word(rom + 0x1bea, 0x0000);
  put_mem_word(rom + 0x1bec, 0x13fc);
  put_mem_word(rom + 0x1bee, 0x0010);
  put_mem_word(rom + 0x1bf0, 0x00ef);
  put_mem_word(rom + 0x1bf2, 0xe7fe);
  put_mem_word(rom + 0x1bf4, 0x13fc);
  put_mem_word(rom + 0x1bf6, 0x0000);
  put_mem_word(rom + 0x1bf8, 0x00ef);
  put_mem_word(rom + 0x1bfa, 0xfffe);
  put_mem_word(rom + 0x1bfc, 0x4ef9);
  put_mem_word(rom + 0x1bfe, 0x0040);
  put_mem_word(rom + 0x1c00, 0x1d04);

  // Patch ROM checksum check at 0x401c6e to nop
  put_mem_word(rom + 0x1c6e, 0x4e71);
  put_mem_word(rom + 0x1c70, 0x4e71);

  // Bypass loop at 0x402a8e (waiting for VIA1 CA1)
  put_mem_word(rom + 0x2a94, 0x4e71);
  put_mem_word(rom + 0x2a96, 0x4e71);

  // Bypass loop at 0x40335e
  put_mem_word(rom + 0x3364, 0x4e71);

  unsigned int driver_base = 0x3F000;
  unsigned char* pto = driver_base + rom;

  memcpy(pto, sony_driver, sizeof(sony_driver));

  // Extension data block follows driver
  unsigned char* datp = pto + sizeof(sony_driver);
  put_mem_word(datp, kcom_callcheck);

  // Write RTS 16 bytes after Extension Block for jDisk target
  put_mem_word(datp + 16, 0x4e75);

  put_mem_word(datp + 2, kExtnSony);
  put_mem_long(datp + 4, kExtn_Block_Base);

  // Provide a way for the driver to find datp
  unsigned int datp_vm = 0x400000 + driver_base + sizeof(sony_driver);
  put_mem_long(ram + 0x0900, datp_vm);
  put_mem_long(ram + 0x0316, datp_vm + 16);

  // Patch the ORIGINAL Sony driver area at 0x34680
  const unsigned int offsets[] = {0x6E, 0x38C, 0x1F6, 0x33E, 0x18};
  const unsigned int dest_offsets[] = {0x00EE, 0x0018, 0x0024, 0x004A, 0x008A};

  for (int i = 0; i < 5; i++) {
    if (i == 0) continue;  // Skip Open patch!
    unsigned char* p = rom + 0x34680 + offsets[i];
    unsigned int target = 0x400000 + driver_base + dest_offsets[i];
    DLOG( "Patching Sony %d at %08x (host %p) -> %08x\n", i,
            (unsigned int)(0x434680 + offsets[i]), p, (unsigned int)target);
    p[0] = 0x24;
    p[1] = 0x7C;  // movea.l #imm, A2
    p[2] = (target >> 24) & 0xFF;
    p[3] = (target >> 16) & 0xFF;
    p[4] = (target >> 8) & 0xFF;
    p[5] = target & 0xFF;
    p[6] = 0x4E;
    p[7] = 0xD2;  // jmp (A2)

    // Also patch the TARGET with a callcheck
    unsigned char* pt = rom + driver_base + dest_offsets[i];
    pt[0] = (kcom_callcheck >> 8) & 0xFF;
    pt[1] = kcom_callcheck & 0xFF;
  }
}

bool RomDevice::Init() {
  DLOG( "RomDevice::Init called\n");
#if UseSonyPatch
  InstallSonyPatch();
#endif
  return true;
}
