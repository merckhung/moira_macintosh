// Helper functions for big-endian writes
static void put_mem_word(unsigned char* p, unsigned short v) {
  p[0] = (v >> 8) & 0xFF;
  p[1] = v & 0xFF;
}

static void put_mem_long(unsigned char* p, unsigned int v) {
  p[0] = (v >> 24) & 0xFF;
  p[1] = (v >> 16) & 0xFF;
  p[2] = (v >> 8) & 0xFF;
  p[3] = v & 0xFF;
}

